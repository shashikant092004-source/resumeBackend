const mongoose = require("mongoose");

const contactUsSchema = new mongoose.Schema(
  {
    fullName: {
      type: String,
      required: true,
      trim: true,
    },

    email: {
      type: String,
      required: true,
      trim: true,
    },

    message: {
      type: String,
      required: true,
      trim: true,
    },
  },

  { timestamps: true },
);

module.exports = mongoose.model("ContactUs", contactUsSchema);
